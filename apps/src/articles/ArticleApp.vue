                    page_category: this.category,
                    page_description: this.description,
                    page_path: this.url,
                    page_content: this.content,
                    page_type: this.type
                },
                {
                    clickToClose: false,
                    width: "90%",
                    height: "auto",
                    minWidth: 250,
                    minHeight: 400,
                    scrollable: true
                }
            );
        }
    }
